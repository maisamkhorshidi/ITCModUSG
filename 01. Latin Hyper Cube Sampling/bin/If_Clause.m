function y=If_Clause(cc)
if sum(min(cc(1:4))>=cc(5))==1 && sum(max(cc(6:8))<=cc(5))==1 && sum(min(cc(6:8))>=max(cc(13:14)))==1 &&...
        sum(min(cc(13:14))>=max(cc(9:10)))==1 && sum(min(cc(9:10))>=max(cc(11:12)))==1 &&...
        sum(min(cc(11:12))>=max(cc(15:16)))==1 && sum(cc(9)>=cc(10))==1 &&...
        sum(cc(11)>=cc(12))==1 && sum(cc(13)>=cc(14))==1 && sum(cc(15)>=cc(16))==1 &&...
        sum(min(cc(1:8))>=max(cc(9:16)))==1 &&...
        sum(min(1./cc(16+1:16+4))>=1./cc(16+5))==1 &&...
        sum(max(1./cc(16+6:16+8))<=1./cc(16+5))==1 &&...
        sum(min(1./cc(16+6:16+8))>=max(1./cc(16+13:16+14)))==1 &&...
        sum(min(1./cc(16+13:16+14))>=max(1./cc(16+9:16+10)))==1 &&...
        sum(min(1./cc(16+9:16+10))>=max(1./cc(16+11:16+12)))==1 &&...
        sum(min(1./cc(16+11:16+12))>=max(1./cc(16+15:16+16)))==1 &&...
        sum(1./cc(16+9)>=1./cc(16+10))==1 &&...
        sum(1./cc(16+11)>=1./cc(16+12))==1 &&...
        sum(1./cc(16+13)>=1./cc(16+14))==1 &&...
        sum(1./cc(16+15)>=1./cc(16+16))==1 &&...
        sum(min(1./cc(16+1:16+8))>=max(1./cc(16+9:16+16)))==1 &&...
        sum(min(cc(32+1:32+4))>=cc(32+5))==1 &&...
        sum(max(cc(32+6:32+8))<=cc(32+5))==1 &&...
        sum(min(cc(32+6:32+8))>=max(cc(32+13:32+14)))==1 &&...
        sum(min(cc(32+13:32+14))>=max(cc(32+9:32+10)))==1 &&...
        sum(min(cc(32+9:32+10))>=max(cc(32+11:32+12)))==1 &&...
        sum(min(cc(32+11:32+12))>=max(cc(32+15:32+16)))==1 &&...
        sum(cc(32+9)>=cc(32+10))==1 &&...
        sum(cc(32+11)>=cc(32+12))==1 &&...
        sum(cc(32+13)>=cc(32+14))==1 &&...
        sum(cc(32+15)>=cc(32+16))==1 &&...
        sum(min(cc(32+1:32+8))>=max(cc(32+9:32+16)))==1
    y=1;
else
    y=0;
end
return